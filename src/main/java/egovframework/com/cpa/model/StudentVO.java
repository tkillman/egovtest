package egovframework.com.cpa.model;

public class StudentVO {

	private int goubun;
	
	private String name;
	
	private String addr;

	public int getGoubun() {
		return goubun;
	}

	public void setGouBun(int gouBun) {
		this.goubun = gouBun;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}
	
	
}
